# Logical Operators
print(True and True)    # True if both operands are true
print(True and False)   # False if one operand is false
print(False and True)   # False if one operand is false
print(False and False)  # False if both operands are false

print("For Or Operator...")
print(True or True) # True if at least one operand is true
print(True or False)    # True if at least one operand is true
print(False or True)    # True if at least one operand is true
print(False or False)   # False if both operands are false

print("Not Operator")
print(not(True) )   
print(not(False) ) 